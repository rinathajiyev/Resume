    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            EmployementHistory history = list.get(tblHistory.getSelectedRow());

            String begin = txtBeginDate.getText();
            String end = txtEndDate.getText();

            java.util.Date beginDate = sdf.parse(begin);
            long lb = beginDate.getTime();
            java.sql.Date bDate = new java.sql.Date(lb);

            java.util.Date endDate = sdf.parse(end);
            long le = endDate.getTime();
            java.sql.Date eDate = new java.sql.Date(le);

            String description = txtAreaDescription.getText();
            String job = (String) cbJobs.getSelectedItem();

            history.setHeader(job);
            history.setJobDescription(description);
            history.setBeginDate(bDate);
            history.setEndDate(eDate);

            historyDao.updateHistory(history);

            fillWindow();
        } catch (ParseException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        EmployementHistory history = list.get(tblHistory.getSelectedRow());
        int index = history.getId();
        historyDao.removeHistory(index);

        fillWindow();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            String begin = txtBeginDate.getText();
            String end = txtEndDate.getText();

            java.util.Date beginDate = sdf.parse(begin);
            long lb = beginDate.getTime();
            java.sql.Date bDate = new java.sql.Date(lb);

            java.util.Date endDate = sdf.parse(end);
            long le = endDate.getTime();
            java.sql.Date eDate = new java.sql.Date(le);

            String description = txtAreaDescription.getText();
            String job = (String) cbJobs.getSelectedItem();

            EmployementHistory history = new EmployementHistory(job, bDate, eDate, description, Config.loggedInUser);
            historyDao.insertHistory(history);

            fillWindow();
        } catch (ParseException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnAddActionPerformed


