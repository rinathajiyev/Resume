    private void fillTable() {
        User user = Config.loggedInUser;
        int index = user.getId();
        list = historyDao.getAllEmploymentHistoryByUserId(index);

        Vector<Vector> rows = new Vector<>();
        for (EmployementHistory history : list) {
            Vector row = new Vector();
            row.add(history.getHeader());
            row.add(history.getJobDescription());
            row.add(history.getBeginDate());
            row.add(history.getEndDate());

            rows.add(row);
        }

        Vector<String> columns = new Vector<>();
        columns.add("Job Name");
        columns.add("Job Description");
        columns.add("Begin Date");
        columns.add("End Date");

        DefaultTableModel model = new DefaultTableModel(rows, columns);
        tblHistory.setModel(model);
    }

